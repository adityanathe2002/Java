class ArraysDeclaration
{
	public static void main(String[] args) 
	{
		// declaration Statements of Arrays
		// datatype, Array Operator, variable
		int [] arr;                // arr --> 1D
		int arr [];                // arr --> 1D
		int [] arr,arr1;           // arr --> 1D, arr1 --> 1D
		int [][] arr;              // arr --> 2D
		int [][] arr,arr1;         // arr --> 2D, arr1 --> 1D
		int []arr [],arr1;         // arr --> 2D, arr1 --> 1D
		int []arr [][],arr1 [];    // arr --> 3D, arr1 --> 2D
		int []arr [],arr1 [],arr2; // arr --> 2D, arr1 --> 2D,arr2 --> 1D
	}
}
